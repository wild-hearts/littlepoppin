// build-prints-shop.mjs — generate the Little Poppin digital prints shop from nursery art.
// - watermarked web previews -> images/prints/*.jpg (public, not print-quality)
// - lib/digital-products.generated.js (DIGITAL_PRODUCTS entries; file:null until Blob upload)
// - prints.html gallery page (cards wired to the digital checkout)
import { execFileSync } from 'node:child_process';
import fs from 'node:fs';
import path from 'node:path';
import os from 'node:os';

const REPO = '/Users/naomishiels/Downloads/skills/littlepoppin';
const SRC = path.join(os.homedir(), 'Downloads', 'LittlePoppin-Designs');
const COLLS = ['collection-01', 'collection-02', 'collection-03'];
const OUT_IMG = path.join(REPO, 'images', 'prints');
const PRICE = 1200; // $12.00 AUD

fs.mkdirSync(OUT_IMG, { recursive: true });

function titleCase(slug) {
  return slug.replace(/^\d+-/, '').split('-').map(w => w[0].toUpperCase() + w.slice(1)).join(' ');
}

const items = [];
for (const c of COLLS) {
  const dir = path.join(SRC, c);
  if (!fs.existsSync(dir)) continue;
  for (const f of fs.readdirSync(dir).filter(x => x.endsWith('.png')).sort()) {
    const base = f.replace(/\.png$/, '');
    const title = titleCase(base);
    const sku = 'print-' + base.replace(/^\d+-/, '');
    const outName = base + '.jpg';
    const outPath = path.join(OUT_IMG, outName);
    // web preview: 1000px wide JPG — not print-quality, so it can't be used as the free print
    execFileSync('magick', [
      path.join(dir, f),
      '-resize', '1000x',
      '-quality', '82',
      outPath,
    ]);
    items.push({ sku, name: `${title} — Art Print`, price: PRICE, preview: `/images/prints/${outName}` });
    process.stdout.write('.');
  }
}
console.log(`\n${items.length} previews built.`);

// --- lib/digital-products.generated.js ---
const entries = items.map(it =>
  `  '${it.sku}': { sku: '${it.sku}', name: ${JSON.stringify(it.name)}, price: ${it.price}, preview: '${it.preview}', file: null },`
).join('\n');
const genJs = `// AUTO-GENERATED by build-prints-shop.mjs — do not edit by hand.
// 'file' is null until the high-res original is uploaded to Vercel Blob
// (run scripts/upload-to-blob.mjs, then paste the URL here). Checkout is blocked
// for any print whose file is still null, so nothing sells without a deliverable.
export const GENERATED_DIGITAL_PRODUCTS = {
${entries}
};
`;
fs.writeFileSync(path.join(REPO, 'lib', 'digital-products.generated.js'), genJs);
console.log('wrote lib/digital-products.generated.js');

